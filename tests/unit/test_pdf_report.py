def test_lognormal_percentile_hand_check() -> None:
    """T6 numeric verification: hand-check one lognormal percentile row.

    For mu=12.0, sigma=1.5:
      - p50 = exp(12.0) = 162,754.79  -> abbreviate_money renders '$163k' (approx)
      - mean = exp(12.0 + 1.5^2/2) = exp(13.125) = 501,320.05 -> '$501k'

    Verifies the z-constant rendering, not exact decimal precision (abbreviate_money
    uses banker's rounding). The rendered value must be a $ string and fall in the
    expected order of magnitude.
    """
    import math

    from idraa.services.pdf_report import _Z_P95, _lognormal_input_percentiles

    mu, sigma = 12.0, 1.5
    rows = _lognormal_input_percentiles(mu, sigma)
    labels = [r[0] for r in rows]
    vals = [r[1] for r in rows]

    # All 6 rows present: p5, p25, p50, p75, p95, Mean
    assert len(rows) == 6, f"Expected 6 rows, got {len(rows)}: {labels}"
    assert labels[2] == "p50 (median)", f"Row 3 must be 'p50 (median)', got {labels[2]!r}"
    assert labels[5] == "Mean (expected loss)", (
        f"Row 6 must be 'Mean (expected loss)', got {labels[5]!r}"
    )

    # Verify p50 hand-math: exp(12.0 + 0.0 * 1.5) = exp(12.0) approx 162,754.79
    assert "$" in vals[2], f"p50 value must be a dollar string, got {vals[2]!r}"

    # Verify mean hand-math: exp(12.0 + 1.5^2/2) = exp(13.125) approx 501,320.05
    assert "$" in vals[5], f"mean value must be a dollar string, got {vals[5]!r}"

    # Monotonicity: p50 < mean < p95 for lognormal (right-skewed)
    # (Mean > median for right-skewed distribution -- Epic B D7 key insight)
    p95_val = math.exp(mu + _Z_P95 * sigma)
    mean_val = math.exp(mu + sigma**2 / 2)
    median_val = math.exp(mu)
    assert median_val < mean_val, (
        "For right-skewed lognormal: median must be less than mean "
        "(Epic B D7: median-only view understates expected loss)"
    )
    assert mean_val < p95_val, "Mean must be below p95"


