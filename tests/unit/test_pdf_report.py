def test_back_page_is_distinct_from_controls_inventory() -> None:
    """T9 CF-2 structural assertion: the final page (back page) contains
    'Generated by Idraa' and the run UUID, and does NOT contain any
    controls-inventory body content (e.g. 'Firewall', 'EDR', 'SIEM').
    The second-to-last page (control inventory) contains 'Firewall' and
    does NOT contain 'Generated by Idraa'.
    """
    pdf = render_executive_pdf(_canonical_data())
    pages = _extract_pages(pdf)
    # Back page = pages[-1]; control inventory = pages[-2]
    back_page = re.sub(r"\s+", " ", pages[-1])
    inventory_page = re.sub(r"\s+", " ", pages[-2])
    # Back page MUST contain the markers
    assert "Generated by Idraa" in back_page, "Back page must contain 'Generated by Idraa'"
    assert "00000000" in back_page, "Back page must contain the short run UUID"
    # Back page must NOT contain controls-inventory body rows
    # (Firewall / EDR / SIEM appear in the canonical fixture's controls_by_domain)
    assert "Firewall" not in back_page, (
        "Back page must NOT contain inventory content — 'Firewall' found on back page"
    )
    assert "EDR" not in back_page, (
        "Back page must NOT contain inventory content — 'EDR' found on back page"
    )
    # Control inventory page must contain inventory content
    assert "Firewall" in inventory_page, "Control-inventory page must contain 'Firewall'"
    # Control inventory page must NOT contain the back-page marker
    assert "Generated by Idraa" not in inventory_page, (
        "Control-inventory page must NOT contain 'Generated by Idraa'"
    )


