def test_per_scenario_table_fits_usable_frame_with_long_names() -> None:
    """Regression: per-scenario table must not overflow the right margin.

    Before the fix, _per_scenario_table passed raw string cells with no
    colWidths, so reportlab sized the Scenario column to the longest name's
    single-line width. A long OT/SCADA scenario name (this user's domain)
    pushed the table's natural width past the ~487pt portrait usable frame,
    clipping the With/Reduction columns off the page edge. Bounding the
    columns + wrapping the name keeps the table within the frame regardless
    of name length. (UAT 2026-06-14.)

    A bounded-colWidths Table's wrap() returns sum(colWidths) regardless of
    content, so the frame-fit assertion alone only guards against a future
    "colWidths removed" regression (the original bug). The geometric cell
    assertions below additionally prove the two behaviours the fix relies on:
    the long name actually WRAPS inside its column, and a large money amount
    fits its column on one line instead of clipping (money cells are
    Paragraphs, so a pathological amount wraps rather than overflowing).
    """
    from reportlab.lib.pagesizes import A4

    from idraa.services.pdf_report import (
        _PS_MONEY_COL_WIDTHS,
        REPORT_STYLE,
        _per_scenario_table,
    )

    one_line = 11.5  # 9pt font, leading 11 — a single line is ~11pt tall
    usable = A4[0] - 2 * REPORT_STYLE.margin_side
    name_w = usable - sum(_PS_MONEY_COL_WIDTHS)
    long_name = (
        "Ransomware encrypts the OT data historian and operator HMI "
        "workstations halting continuous production lines"
    )
    long_rows = [
        # base_ale at ~$999B exercises the large-org AGGREGATE one-line ceiling
        PerScenarioRow("s1", long_name, 999_000_000_000.0, 4_100_000.0, 5_700_000.0),
        PerScenarioRow(
            "s2",
            "Privileged maintenance insider exfiltrates SCADA controller "
            "configuration and proprietary process recipe IP",
            6_600_000.0,
            3_300_000.0,
            3_300_000.0,
        ),
    ]
    table = _per_scenario_table(long_rows, "$")
    # Capture body-cell flowables BEFORE table.wrap() — reportlab replaces
    # _cellvalues with expanded cell tuples during wrap(), so grab the
    # Paragraph references first. (On the pre-fix code these are plain strings;
    # the frame-fit assertion below fails first, so they are never .wrap()'d.)
    name_cell = table._cellvalues[1][0]  # body row 0, Scenario column
    money_cell = table._cellvalues[1][1]  # body row 0, "Without $" column

    # (1) table bounded to the usable frame — guards against colWidths removal
    width, _height = table.wrap(usable, 10_000)
    assert width <= usable + 0.5, f"table width {width:.0f}pt overflows usable frame {usable:.0f}pt"

    # (2) the long scenario name actually wraps to >1 line inside its column
    _nw, name_h = name_cell.wrap(name_w, 10_000)
    assert name_h > one_line, f"long name did not wrap (height {name_h:.0f}pt)"

    # (3) a ~$999B amount fits its money column on a single line (no clip).
    # Subtract reportlab's default 6+6pt cell padding so the wrap models the
    # real in-table effective width — keeps the guard production-faithful if
    # the columns are ever narrowed.
    effective_money_w = _PS_MONEY_COL_WIDTHS[0] - 12
    _mw, money_h = money_cell.wrap(effective_money_w, 10_000)
    assert money_h <= one_line, (
        f"large amount clipped/wrapped in money column (height {money_h:.0f}pt)"
    )


