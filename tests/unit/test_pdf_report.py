def _extract_pages(pdf_bytes: bytes) -> list[str]:
    """Return per-page extracted text for substring assertions."""
    reader = pypdf.PdfReader(io.BytesIO(pdf_bytes))
    return [p.extract_text() for p in reader.pages]


