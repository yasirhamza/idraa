    assumptions_text = re.sub(r"\s+", " ", pages[assumptions_page_idx])
    assert "Scenario: Transportation & Logistics" in assumptions_text, (
        "Assumptions & inputs page must contain 'Scenario: Transportation & Logistics' "
        "with a literal ampersand — _h1 heading must not double-escape '&' to '&amp;'."
    )
    assert "Scenario: Transportation &amp;" not in assumptions_text, (
        "Assumptions & inputs page must NOT contain 'Scenario: Transportation &amp;' — "
        "double-escape regression: the _h1 call site must pass raw sc_name, not "
        "rl_escape(sc_name), so that _h1's internal escape is the only one that fires."
    )


