# ---- lognormal_mixture percentiles (issue #27 Task 6) ----
#
# Catastrophic multi-SME pl/sl stores a linear-opinion-pool MIXTURE
# ({"components": [{"mean", "sigma", "weight"}, ...]}), not a single
# {mean, sigma} pair — see wizard_finalize.build_scenario_payload. Mixture
# percentiles have NO closed form (unlike the single-lognormal z-constant
# rows above): they are the inverse of the pooled CDF Sigma w_i F_i(x),
# solved by bisection.
#
# fair_cam.quantile_pooling.mixture_quantile_lognorm already implements this
# exact bisection (scipy.stats.truncnorm-based, since fair_cam's mixture
# components carry min_support/max_support). This module CANNOT import it:
# services/pdf_report.py is a fair_cam-free/DB-free/HTTP-free pure renderer
# by module convention (see the module docstring and
# test_pdf_report_purity_no_db_or_routes_or_fair_cam_imports) — a
# function-local/deferred fair_cam import would dodge that specific test
# (which only inspects sys.modules after a plain `import
# idraa.services.pdf_report`, not after a render call) without honoring the
# invariant it exists to enforce, so the algorithm is mirrored locally
# instead. Catastrophic mixture components are stored NATIVE/UNTRUNCATED
# (see lognormal_mixture_display_rows' storage note in app.py: "non-binding
# [0, inf] truncation ... each component's (meanlog, sdlog) IS its native
# untruncated {mean, sigma}"), so the per-component CDF has a closed form
# via math.erf — no scipy dependency needed either.
_SQRT_2: float = math.sqrt(2.0)

# Bisection bracket + tolerance mirror fair_cam's
# MIXTURE_BISECT_MAX_ITER/MIXTURE_BISECT_REL_TOL (quantile_pooling/_types.py)
# in magnitude, but as a FIXED wide log-space range rather than geometric
# widening from component quantiles (this module has no scipy norm.ppf to
# seed a per-component bracket). [-100, 300] safely brackets any
# store-validated component: fair_cam_validation._SIGMA_MAX caps sigma at
# 10, and realistic USD catastrophic-loss meanlog stays well under 100 —
# worst case (sigma=10, meanlog=30) gives z ~= -13 at the low bound and
# z ~= 27 at the high bound, both already CDF-saturated at 0/1.
_MIXTURE_BISECT_LOG_LO: float = -100.0
_MIXTURE_BISECT_LOG_HI: float = 300.0
_MIXTURE_BISECT_MAX_ITER: int = 200
_MIXTURE_BISECT_LOG_TOL: float = 1e-10


def _mixture_lognorm_cdf(x: float, components: Sequence[dict[str, float]]) -> float:
    """CDF of an untruncated lognormal MIXTURE, Sigma w_i F_i(x), at x.

    F_i(x) = Phi((ln x - mean_i) / sigma_i), Phi(z) = 0.5*(1 + erf(z/sqrt2))
    — the closed-form untruncated-lognormal CDF (no scipy needed)."""
    if x <= 0.0:
        return 0.0
    return sum(
        c["weight"] * 0.5 * (1.0 + math.erf(((math.log(x) - c["mean"]) / c["sigma"]) / _SQRT_2))
        for c in components
    )


def _mixture_lognorm_quantile(p: float, components: Sequence[dict[str, float]]) -> float:
    """Quantile of the lognormal mixture at probability p — log-space
    bisection on the pooled CDF (see the module-comment block above for why
    this mirrors fair_cam.quantile_pooling.mixture_quantile_lognorm's
    algorithm rather than importing it)."""
    if not 0.0 < p < 1.0:
        raise ValueError(f"p must be in (0, 1), got {p}")
    lo_log, hi_log = _MIXTURE_BISECT_LOG_LO, _MIXTURE_BISECT_LOG_HI
    for _ in range(_MIXTURE_BISECT_MAX_ITER):
        if (hi_log - lo_log) < _MIXTURE_BISECT_LOG_TOL:
            break
        mid_log = (lo_log + hi_log) / 2.0
        if _mixture_lognorm_cdf(math.exp(mid_log), components) < p:
            lo_log = mid_log
        else:
            hi_log = mid_log
    return math.exp((lo_log + hi_log) / 2.0)


def _lognormal_mixture_percentiles(
    components: Sequence[dict[str, float]],
    reporting_code: str = "USD",
    fx_rate: float = 1.0,
) -> list[tuple[str, str]]:
    """Return (label, formatted_value) rows for a lognormal_mixture INPUT
    distribution (issue #27 Task 6) — MIXTURE percentiles (the inverse of
    the pooled CDF), NOT a per-component average.

    Currency conversion: same post-exponentiation multiply-by-fx_rate
    convention as _lognormal_input_percentiles above — lognormal_mixture is
    confined to catastrophic pl/sl (always a loss-magnitude/dollar node,
    never vuln/frequency; see wizard_finalize.build_scenario_payload), so
    this mirrors the loss-magnitude branch's conversion, not the vuln/freq
    currency-invariant ones.

    Mean row: analytic Sigma w_i * exp(mean_i + sigma_i**2/2) — exact, no
    bisection needed (same formula lognormal_mixture_display_rows uses in
    app.py, and the same standard lognormal-expectation formula as the
    single-component Mean rows above).
    """

    def _fmt(usd_val: float) -> str:
        return safe_money_format(usd_val * fx_rate, reporting_code, compact=True)

    rows: list[tuple[str, str]] = [
        ("p5", _fmt(_mixture_lognorm_quantile(0.05, components))),
        ("p25", _fmt(_mixture_lognorm_quantile(0.25, components))),
        ("p50 (median)", _fmt(_mixture_lognorm_quantile(0.50, components))),
        ("p75", _fmt(_mixture_lognorm_quantile(0.75, components))),
        ("p95", _fmt(_mixture_lognorm_quantile(0.95, components))),
        (
            "Mean (expected loss)",
            _fmt(
                sum(c["weight"] * math.exp(c["mean"] + c["sigma"] ** 2 / 2.0) for c in components)
            ),
        ),
    ]
    return rows


def _draw_distribution_table(
    node_name: str,
    dist_dict: dict[str, Any] | None,
    styles: Any,
    is_vulnerability: bool = False,
    is_frequency: bool = False,
    sym: str = "$",  # kept for API compatibility; money formatting uses safe_money_format
    reporting_code: str = "USD",  # P3 B1: reporting currency code for loss-magnitude nodes ONLY
    fx_rate: float = 1.0,  # P3 B1: USD→reporting rate; 1.0 for USD (identity)
) -> list[Any]:
    """Render a single FAIR input distribution as a percentile-style table.

    T6 (#351): Per Epic B display decision:
    - lognormal → p5/p25/p50/p75/p95 + Mean (expected loss) row
    - lognormal_mixture (issue #27 Task 6) → same row shape, but MIXTURE
      percentiles (bisection on the pooled CDF), not a per-component average
    - PERT → low / most-likely / high labeled "(elicited values: low / most-likely / high)"

    Table heading: "Input distribution percentiles (parametric)" for lognormal
    to distinguish from §4's empirical output table. PERT rows carry the
    elicited-values label.
    """
    if dist_dict is None:
        return [Paragraph(f"{rl_escape(node_name)}: not set", styles["Normal"])]

    dist_type = str(dist_dict.get("distribution", "")).upper()

    # numeric_cols=[1]: "Value" is the second column in both the Percentile/Value
    # and Parameter/Value layouts below — plain-string cells, so TableStyle ALIGN
    # (unlike the Paragraph-cell tables) works directly.
    dist_table_style = pdf_theme.table_style(numeric_cols=[1])
    heading3_style = pdf_theme.para("h3", fontSize=9, spaceAfter=2)
    note_style = pdf_theme.para("micro", fontSize=7, spaceAfter=4)

    flowables_out: list[Any] = []
    flowables_out.append(Paragraph(rl_escape(node_name), heading3_style))

    if dist_type == "LOGNORMAL":
        mu_val = float(dist_dict.get("mean", 0.0) or 0.0)
        sigma_val = float(dist_dict.get("sigma", 1.0) or 1.0)
        dist_label = f"Lognormal (mean-log={mu_val:.4f}, sigma={sigma_val:.4f})"
        flowables_out.append(
            Paragraph(
                f"Distribution type: {rl_escape(dist_label)} — "
                "Input distribution percentiles (parametric)",
                note_style,
            )
        )
        if is_vulnerability:
            # CURRENCY-INVARIANT: probabilities (0-1), NOT dollar amounts — no conversion.
            rows = _lognormal_vuln_percentiles(mu_val, sigma_val)
        elif is_frequency:
            # CURRENCY-INVARIANT: events/year, NOT dollar amounts — no conversion.
            rows = _lognormal_freq_percentiles(mu_val, sigma_val)
        else:
            # LOSS-MAGNITUDE: dollar amounts — convert POST-exponentiation (B1).
            # fx_rate=1.0 for USD (identity); non-1 for non-USD reporting currencies.
            rows = _lognormal_input_percentiles(mu_val, sigma_val, reporting_code, fx_rate)
        table_data = [["Percentile", "Value"]] + [[label, val] for label, val in rows]
        flowables_out.append(Table(table_data, colWidths=[130, 120], style=dist_table_style))
    elif dist_type == "LOGNORMAL_MIXTURE":
        # Issue #27 Task 6: catastrophic multi-SME pl/sl — a linear-opinion-
        # pool mixture, NOT a single {mean, sigma} pair. Coerce to float here
        # (same type-coercion-only contract as run_executor._dict_to_fair_distribution;
        # shape/finiteness validation already happened at store time).
        components: list[dict[str, float]] = [
            {
                "mean": float(c["mean"]),
                "sigma": float(c["sigma"]),
                "weight": float(c["weight"]),
            }
            for c in dist_dict.get("components") or []
        ]
        dist_label = f"Lognormal mixture ({len(components)} expert opinions)"
        flowables_out.append(
            Paragraph(
                f"Distribution type: {rl_escape(dist_label)} — "
                "Input distribution percentiles (parametric, pooled mixture)",
                note_style,
            )
        )
        # is_vulnerability/is_frequency are not passed for this branch —
        # lognormal_mixture is confined to catastrophic pl/sl (always
        # loss-magnitude), so this always converts via fx_rate, mirroring
        # the LOGNORMAL branch's "else" (loss-magnitude) arm above.
        rows = _lognormal_mixture_percentiles(components, reporting_code, fx_rate)
        table_data = [["Percentile", "Value"]] + [[label, val] for label, val in rows]
        flowables_out.append(Table(table_data, colWidths=[130, 120], style=dist_table_style))
    elif dist_type == "PERT":
        low = dist_dict.get("low")
        mode = dist_dict.get("mode")
        high = dist_dict.get("high")
        dist_label = "PERT"
        flowables_out.append(
            Paragraph(
                f"Distribution type: {rl_escape(dist_label)} "
                "(elicited values: low / most-likely / high)",
                note_style,
            )
        )
        if is_vulnerability:
            rows_pert = [
                ("Low", _format_vuln_value(float(low)) if low is not None else "—"),
                (
                    "Most-likely (mode)",
                    _format_vuln_value(float(mode)) if mode is not None else "—",
                ),
                ("High", _format_vuln_value(float(high)) if high is not None else "—"),
            ]
        elif is_frequency:
            rows_pert = [
                ("Low", _format_freq_value(float(low)) if low is not None else "—"),
                (
                    "Most-likely (mode)",
                    _format_freq_value(float(mode)) if mode is not None else "—",
                ),
                ("High", _format_freq_value(float(high)) if high is not None else "—"),
            ]
        else:
            # LOSS-MAGNITUDE: dollar amounts — multiply by fx_rate then format.
            # PERT params (low/mode/high) are stored in USD; convert directly.
            # T5a: compact=True — no-cents $B/$M/$k, matching the lognormal
            # branch's percentile rows (_lognormal_input_percentiles) above.
            rows_pert = [
                (
                    "Low",
                    safe_money_format(float(low) * fx_rate, reporting_code, compact=True)
                    if low is not None
                    else "—",
                ),
                (
                    "Most-likely (mode)",
                    safe_money_format(float(mode) * fx_rate, reporting_code, compact=True)
                    if mode is not None
                    else "—",
                ),
                (
                    "High",
                    safe_money_format(float(high) * fx_rate, reporting_code, compact=True)
                    if high is not None
                    else "—",
                ),
            ]
        table_data = [["Parameter", "Value"]] + [[label, val] for label, val in rows_pert]
        flowables_out.append(Table(table_data, colWidths=[130, 120], style=dist_table_style))
    else:
        flowables_out.append(
            Paragraph(
                f"Distribution: {rl_escape(str(dist_dict)[:80])}",
                styles["Normal"],
            )
        )

    return flowables_out


def _draw_controls_assignment_snapshot_table(
    controls_snapshot: list[dict[str, Any]],
    styles: Any,
) -> list[Any]:
    """Render the control-assignment snapshot summary table.

    T6 (#351): sourced from run.controls_snapshot (NOT live controls — the
    snapshot is the as-executed state). unit_type included per the methodology
    re-gate: capability_value is uninterpretable without it.

    Columns: control name, sub-function, capability, unit type, coverage,
    reliability, confirmed.

    For V3 snapshots each control has a list of assignments; for older V2
    snapshots assignments may be empty or absent.
    """
    if not controls_snapshot:
        return [
            Paragraph(
                "No control assignments recorded in this run snapshot.",
                styles["Normal"],
            )
        ]

    # FONTSIZE 7 for this table's grid is not needed — the SnapCell Paragraph
    # style below (fontSize=7) carries the tighter text size; table_style's
    # grid font (8) governs plain-string cells only ("—" placeholders).
    snap_table_style = pdf_theme.table_style()
    header = [
        "Control",
        "Sub-function",
        "Capability",
        "Unit type",
        "Coverage",
        "Reliability",
        "Confirmed",
    ]
    # P8: the Control and Sub-function cells hold free text that can exceed the
    # column width. Plain-string Table cells do NOT wrap — they render on one line
    # and overprint the neighbouring column. Wrapping them in Paragraph flowables
    # (with explicit colWidths, unchanged) makes the text reflow inside the column.
    ps_cell = pdf_theme.para("micro", fontSize=7, leading=8)
    rows: list[list[Any]] = []
    for snap in controls_snapshot:
        ctrl_name = Paragraph(rl_escape(str(snap.get("name", ""))), ps_cell)
        assignments = snap.get("assignments") or []
        if not assignments:
            rows.append([ctrl_name, "—", "—", "—", "—", "—", "—"])
        else:
            for i, asn in enumerate(assignments):
                name_cell: Any = ctrl_name if i == 0 else ""
                sub_fn = Paragraph(rl_escape(str(asn.get("sub_function", "—"))), ps_cell)
                cap = asn.get("capability_value")
                cap_str = f"{cap:.3f}" if cap is not None else "—"
                unit_type = rl_escape(str(asn.get("unit_type", "—")))
                coverage = asn.get("coverage")
                cov_str = f"{coverage:.0%}" if coverage is not None else "—"
                reliability = asn.get("reliability")
                rel_str = f"{reliability:.0%}" if reliability is not None else "—"
                confirmed = asn.get("confirmed_by_user_at")
                conf_str = "Yes" if confirmed else "No"
                rows.append([name_cell, sub_fn, cap_str, unit_type, cov_str, rel_str, conf_str])

    table_data = [header, *rows]
    col_widths = [100, 80, 55, 60, 50, 55, 50]
    t = Table(table_data, colWidths=col_widths, style=snap_table_style, repeatRows=1)
    return [t]


def _draw_single_scenario_inputs(
    scenario_dict: dict[str, Any],
    styles: Any,
    sym: str,
    *,
    scenario_label: str | None = None,
    suppress_name_heading: bool = False,
    reporting_code: str = "USD",  # P3 B1: passed to loss-magnitude distribution tables
    fx_rate: float = 1.0,  # P3 B1: USD→reporting rate; 1.0 for USD (identity)
) -> list[Any]:
    """Render input distributions for one scenario.

    T6 (#351): renders TEF, Vulnerability, Primary Loss, Secondary Loss as
    percentile-style tables. Vulnerability uses probability formatting (0-1 as %).

    P9: when ``suppress_name_heading`` is True the caller has already emitted a
    section heading for this scenario (the AGGREGATE "Scenario: X" H2), so the
    redundant per-scenario name H3 is dropped to avoid a doubled heading.
    """
    name = rl_escape(str(scenario_dict.get("scenario_name", "(unnamed)")))
    heading_label = scenario_label or name
    flowables_out: list[Any] = []
    if suppress_name_heading:
        pass
    elif scenario_label:
        flowables_out.append(Paragraph(rl_escape(heading_label), pdf_theme.para("h3")))
    else:
        flowables_out.append(Paragraph(name, pdf_theme.para("h3")))

    flowables_out += _draw_distribution_table(
        "Threat Event Frequency (TEF)",
        scenario_dict.get("threat_event_frequency"),
        styles,
        is_vulnerability=False,
        is_frequency=True,  # CURRENCY-INVARIANT: frequency (events/yr) — fx_rate NOT passed
        sym=sym,
        reporting_code=reporting_code,
        fx_rate=1.0,  # explicit identity; frequency is not currency-denominated
    )
    flowables_out.append(Spacer(1, 0.05 * inch))

    flowables_out += _draw_distribution_table(
        "Vulnerability",
        scenario_dict.get("vulnerability"),
        styles,
        is_vulnerability=True,  # CURRENCY-INVARIANT: probability (0-1) — fx_rate NOT passed
        sym=sym,
        reporting_code=reporting_code,
        fx_rate=1.0,  # explicit identity; probability is not currency-denominated
    )
    flowables_out.append(Spacer(1, 0.05 * inch))

    flowables_out += _draw_distribution_table(
        "Primary Loss",
        scenario_dict.get("primary_loss"),
        styles,
        is_vulnerability=False,  # LOSS-MAGNITUDE: dollars → apply fx_rate
        sym=sym,
        reporting_code=reporting_code,
        fx_rate=fx_rate,
    )
    flowables_out.append(Spacer(1, 0.05 * inch))

    sec_loss = scenario_dict.get("secondary_loss")
    if sec_loss is not None:
        flowables_out += _draw_distribution_table(
            "Secondary Loss",
            sec_loss,
            styles,
            is_vulnerability=False,  # LOSS-MAGNITUDE: dollars → apply fx_rate
            sym=sym,
            reporting_code=reporting_code,
            fx_rate=fx_rate,
        )
        flowables_out.append(Spacer(1, 0.05 * inch))

    return flowables_out


def _draw_assumptions_inputs_page(data: RunReportData, styles: Any) -> list[Any]:
    """Assumptions & inputs section — T6 (#351).

    Per scenario:
    (a) Input distribution percentile tables (TEF, Vulnerability, PL, SL) —
        lognormal: p5/p25/p50/p75/p95 + Mean (expected loss) row;
        PERT: elicited values (low / most-likely / high).
        Table label: "Input distribution percentiles (parametric)".
    (b) Control-assignment snapshot summary (from run.controls_snapshot).
    (c) Org context (industry, revenue tier, loss tolerance / "not set") +
        run metadata (n_simulations, engine, run type, completed_at UTC).
    (d) AGGREGATE: per-scenario headings registered in TOC.
    (e) XSS: all user-supplied strings through rl_escape().
    (f) Snapshot-backed: as-executed; legacy-null: live values under honest label.
    """
    _rc = getattr(data, "reporting_code", "USD")
    _sym = getattr(data, "reporting_symbol", "$")
    run_type = getattr(data, "run_type", "aggregate")
    scenario_inputs = getattr(data, "scenario_inputs", None) or {}
    label = scenario_inputs.get("label", "as-executed")
    scenarios_list = scenario_inputs.get("scenarios", [])
    controls_snapshot = list(getattr(data, "controls_snapshot", None) or [])

    note_style = pdf_theme.para("caption", fontSize=8, spaceAfter=4)
    meta_style = pdf_theme.para("body", fontSize=9, spaceAfter=3)

    flowables: list[Any] = [
        _h1("Assumptions & inputs", styles),
        Spacer(1, 0.1 * inch),
    ]

    # ---- (c) Org context + run metadata ----
    org_name = rl_escape(str(data.org.name))
    industry = rl_escape(_humanize_enum(str(getattr(data.org, "industry_type", "") or "")))
    revenue = getattr(data.org, "annual_revenue", None)
    if revenue:
        _rev_rate = getattr(data, "reporting_rate", 1.0)
        revenue_str = safe_money_format(float(revenue) * _rev_rate, _rc, compact=True)
    else:
        revenue_str = "not set"
    lt = getattr(data, "loss_tolerance", None)
    if lt:
        # loss_tolerance["amount"] is already converted at the builder (reports.py)
        tol_str = (
            f"{safe_money_format(lt['amount'], _rc, compact=True)} @ {lt['probability'] * 100:.1f}%"
        )
    else:
        tol_str = "not set"

    completed = data.run.completed_at.strftime("%Y-%m-%d %H:%M") + " UTC"
    run_type_label = "Single scenario" if run_type == "single" else "Aggregate"

    flowables.append(Paragraph("Organisation & run context", pdf_theme.para("h3")))
    for line_label, line_val in [
        ("Organisation", org_name),
        ("Industry", industry or "—"),
        ("Annual revenue", revenue_str),
        ("Loss tolerance", tol_str),
        ("Run type", rl_escape(run_type_label)),
        ("Completed at", rl_escape(completed)),
        ("Simulations (n)", f"{data.n_simulations:,}"),
        (
            "Random seed",
            str(data.random_seed)
            if getattr(data, "random_seed", None) is not None
            else "not recorded",
        ),
        ("Engine", rl_escape(getattr(data, "engine_label", "fair-cam"))),
    ]:
        flowables.append(Paragraph(f"<b>{rl_escape(line_label)}:</b> {line_val}", meta_style))

    flowables.append(Spacer(1, 0.15 * inch))

    # ---- P3: currency-provenance note (non-USD runs only) ----
    _provenance = getattr(data, "currency_provenance", None)
    if _provenance:
        prov_style = pdf_theme.para("caption", fontSize=8, spaceAfter=4)
        flowables.append(
            Paragraph(
                rl_escape(_provenance),
                prov_style,
            )
        )

    # ---- Snapshot label / honest-label note ----
    if label != "as-executed":
        flowables.append(
            Paragraph(
                rl_escape(label),
                note_style,
            )
        )
    flowables.append(Spacer(1, 0.1 * inch))

    # ---- (a) Per-scenario input distribution tables ----
    # P9: this heading is a section lead-in for the per-scenario blocks below; a
    # slightly larger gap keeps it from reading as an orphan directly stacked on
    # the first "Scenario: X" heading.
    flowables.append(Paragraph("Input distributions (per scenario)", pdf_theme.para("h3")))
    flowables.append(Spacer(1, 0.12 * inch))

    if not scenarios_list:
        flowables.append(Paragraph("No scenario input data available.", styles["Normal"]))
    else:
        for sc_dict in scenarios_list:
            sc_name = str(sc_dict.get("scenario_name", "(unnamed)"))
            _aggregate = run_type != "single"
            if _aggregate:
                # AGGREGATE: register each scenario heading in the TOC
                flowables.append(_h1(f"Scenario: {sc_name}", styles))
            _fx_rate = getattr(data, "reporting_rate", 1.0)
            # P9: in AGGREGATE the "Scenario: X" H2 above already names the
            # scenario, so suppress the inner name H3 (drops the doubled heading).
            flowables += _draw_single_scenario_inputs(
                sc_dict,
                styles,
                _sym,
                suppress_name_heading=_aggregate,
                reporting_code=_rc,
                fx_rate=_fx_rate,
            )
            flowables.append(Spacer(1, 0.1 * inch))

    flowables.append(Spacer(1, 0.1 * inch))

    # ---- (b) Control-assignment snapshot summary ----
    flowables.append(Paragraph("Control-assignment snapshot", pdf_theme.para("h3")))
    flowables.append(
        Paragraph(
            "Values as captured at execution time (snapshot, not live controls). "
            "Unit type preserved to make capability values interpretable.",
            note_style,
        )
    )
    flowables.append(Spacer(1, 0.05 * inch))
    flowables += _draw_controls_assignment_snapshot_table(controls_snapshot, styles)

    return flowables


