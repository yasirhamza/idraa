def _draw_methodology_provenance_appendix(data: RunReportData, styles: Any) -> list[Any]:
    """Methodology & provenance appendix — T7 (#351).

    Sections:
    (A) Per-scenario provenance blocks:
        - Loss-tier badge from TIER_BADGE_TEXT (unknown/missing tier → omit badge line)
        - Calibration-anchor text
        - Source citations as plain text list (no links, no /URI entries)
        - Vulnerability-posture line: inherent/pre-control framing (#339)
        - Library-derived scenarios: IRIS-anchor caveat verbatim
        - Analyst-authored: exact fallback "analyst-authored — no library provenance"
    (B) FAIR node glossary (from FAIR_GLOSSARY in reports.py — no hardcoded copy)
    (C) Simulation-method note (Monte Carlo, n_simulations, empirical-percentile language)

    Security: all user-derived strings pass through rl_escape().
    The per-iteration sample-array table must never be referenced here
    (purity boundary: no DB, no ORM, no per-iteration arrays in the renderer).
    """
    note_style = pdf_theme.para("caption", fontSize=8, spaceAfter=3)
    body_style = pdf_theme.para("body", fontSize=9, spaceAfter=4, leading=13)
    badge_style = pdf_theme.para(
        "body",
        fontSize=8,
        # Minor-4 (final-review): intentional one-off raw hex — provenance badge,
        # amber-800, no PDFColors equivalent (status_warning is #B45309, a
        # different shade). Not worth a single-use token.
        textColor=colors.HexColor("#92400E"),
        spaceAfter=2,
    )
    bullet_style = pdf_theme.para("body", fontSize=8, leftIndent=12, spaceAfter=2)
    sub_heading_style = pdf_theme.para("h3", fontSize=10, spaceAfter=4, spaceBefore=8)

    flowables: list[Any] = [
        _h1("Methodology & provenance appendix", styles),
        Spacer(1, 0.15 * inch),
    ]

    # ---- (A) Per-scenario provenance ----
    scenario_provenance = list(getattr(data, "scenario_provenance", None) or [])
    if scenario_provenance:
        flowables.append(Paragraph("Scenario provenance", sub_heading_style))
        for prov in scenario_provenance:
            sc_name = rl_escape(str(prov.get("scenario_name", "(unknown)")))
            flowables.append(Paragraph(f"<b>{sc_name}</b>", body_style))

            fallback_label = prov.get("provenance_label")
            if fallback_label:
                # Analyst-authored — exact fallback text
                flowables.append(Paragraph(rl_escape(str(fallback_label)), note_style))
                # Vulnerability-posture line for analyst-authored scenarios (#351)
                vuln_posture = (
                    "Vulnerability (inherent, pre-control baseline):"
                    " analyst-authored — no library provenance"
                )
                flowables.append(Paragraph(rl_escape(vuln_posture), note_style))
            else:
                # Library-derived
                loss_tier = prov.get("loss_tier")
                if loss_tier and loss_tier in TIER_BADGE_TEXT:
                    # Badge from TIER_BADGE_TEXT — no hardcoded copy in the renderer
                    badge_text = TIER_BADGE_TEXT[loss_tier]
                    flowables.append(Paragraph(rl_escape(badge_text), badge_style))

                calibration_anchor = prov.get("calibration_anchor")
                if calibration_anchor:
                    flowables.append(
                        Paragraph(
                            f"Calibration anchor: {rl_escape(str(calibration_anchor))}",
                            body_style,
                        )
                    )

                source_citations = list(prov.get("source_citations") or [])
                if source_citations:
                    flowables.append(Paragraph("Source citations:", body_style))
                    for cit in source_citations:
                        # SECURITY: plain-text only — no links, no /URI entries.
                        # rl_escape() converts any < > & to entities so reportlab
                        # sees inert text, not markup. javascript: URLs become
                        # harmless literal strings.
                        flowables.append(
                            Paragraph(
                                f"• {rl_escape(str(cit))}",
                                bullet_style,
                            )
                        )

                # Vulnerability-posture line: inherent/pre-control framing (#339)
                if loss_tier is not None:
                    # Library-derived: carry IRIS-anchor caveat verbatim
                    vuln_posture = (
                        "Vulnerability (inherent, pre-control baseline):"
                        " controlled-world prior adopted as inherent baseline, optimistic direction"
                    )
                else:
                    vuln_posture = (
                        "Vulnerability (inherent, pre-control baseline): analyst-specified"
                    )
                flowables.append(Paragraph(rl_escape(vuln_posture), note_style))

            flowables.append(Spacer(1, 0.08 * inch))

    # ---- (B) FAIR node glossary ----
    flowables.append(Paragraph("FAIR node glossary", sub_heading_style))
    # Consume from FAIR_GLOSSARY (defined in reports.py) — NO hardcoded copy here.
    for term in ("TEF", "Vulnerability", "Loss Magnitude", "ALE", "VaR", "ES"):
        definition = FAIR_GLOSSARY.get(term, "")
        if definition:
            flowables.append(
                Paragraph(
                    f"<b>{rl_escape(term)}:</b> {rl_escape(definition)}",
                    body_style,
                )
            )

    # ---- (C) Simulation-method note ----
    flowables.append(Spacer(1, 0.1 * inch))
    flowables.append(Paragraph("Simulation method", sub_heading_style))
