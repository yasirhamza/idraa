def _lec_chart(data: RunReportData, *, width: float = 480.0) -> Drawing:
    """Dual LEC plot — log-x (loss) + linear-y (%) matching the web macro
    in templates/macros/chart.html::dual_lec_curve.

    log-x compresses the loss dynamic range so the with-controls curve
    doesn't collapse into a vertical line when the without-controls range
    is two orders of magnitude larger. linear-y in % matches the pyfair
    convention for the probability axis and reads decision-cleanly (P at
    the threshold is the metric leaders care about).

    ReportLab's LogValueAxis isn't LinePlot-compatible (no joinAxis), so
    we pre-transform x to log10 and use a labelTextFormat lambda to round-
    trip the tick labels back to ``$X``. Y stays linear ``0..1`` with
    ``X%`` formatting.

    ``width`` parameterizes the outer Drawing width so the chart can render
    full-width on the LEC-only page (default 500) or half-width inside a
    side-by-side Table cell (250) without distortion. ``plot.width`` derives
    from the outer width minus a fixed left margin (60) and right margin
    (40), so the chart's internal proportions stay consistent.
    """
    import math

    drawing = Drawing(width, 280)
    plot = LinePlot()
    plot.x = 60
    plot.y = 50
    plot.width = max(width - 100.0, 100.0)
    plot.height = 200

    floor_x = 1.0

    def _log_x(pts: list[tuple[float, float]]) -> list[tuple[float, float]]:
        return [(math.log10(max(x, floor_x)), y) for (x, y) in pts]

    plot.data = [_log_x(data.lec_without), _log_x(data.lec_with)]
    plot.lines[0].strokeColor = PDF_CHART["inherent"]  # without controls
    plot.lines[1].strokeColor = PDF_CHART["residual"]  # with controls

    # UAT 2026-05-21: abbreviated currency (matches dashboard's
    # abbreviate_money Jinja filter) so the log-x ticks "$3.16M $10M
    # $31.6M $100M" don't overlap each other the way the prior
    # "$3,162,278 $10,000,000 ..." rendering did. P3: use reporting_code
    # so EUR runs show "€" instead of "$".
    _lec_rc = getattr(data, "reporting_code", "USD")
    plot.xValueAxis.labelTextFormat = lambda v: safe_money_format(10**v, _lec_rc, compact=True)
    plot.yValueAxis.valueMin = 0.0
    plot.yValueAxis.valueMax = 1.0
    plot.yValueAxis.labelTextFormat = lambda v: f"{v * 100:.0f}%"
    drawing.add(plot)

    # Tolerance overlay — log-x for vertical line, linear-y for horizontal.
    # Web-aligned (chart.html::dual_lec_curve): two crossed dashed lines + corner
    # labels, NO shaded region (see the block below for the rationale).
    tolerance = getattr(data, "loss_tolerance", None)
    # Issue #265: gate on `is not None`, not bare truthiness — a valid 0.0
    # amount/probability is meaningful and must not be dropped. Mirrors the
    # producer (reports.py:519-526), which uses `is not None`.
    if (
        tolerance
        and tolerance.get("amount") is not None
        and tolerance.get("probability") is not None
    ):
        all_xs = [math.log10(max(x, floor_x)) for (x, _y) in data.lec_without + data.lec_with]
        if all_xs:
            xmin, xmax = min(all_xs), max(all_xs)
            tol_amt = tolerance["amount"]
            tol_prob = tolerance["probability"]
            x_log = math.log10(max(tol_amt, floor_x))
            x_frac = (x_log - xmin) / (xmax - xmin) if xmax > xmin else 0.0
            v_x = plot.x + x_frac * plot.width
            h_y = plot.y + tol_prob * plot.height
            # Web-alignment (chart.html::dual_lec_curve): the LEC carries NO shaded
            # region — the web deliberately conveys the unacceptable quadrant with
            # the two crossed dashed lines + labels only (chart.html:385-389). The
            # prior PDF-only red shaded Rect diverged from the web and read as a
            # heavy block dominating the chart, so it is removed.
            v_line = Line(v_x, plot.y, v_x, plot.y + plot.height)
            v_line.strokeColor = PDF_CHART["tolerance"]
            v_line.strokeWidth = 0.8
            v_line.strokeDashArray = [2, 2]
            drawing.add(v_line)
            h_line = Line(plot.x, h_y, plot.x + plot.width, h_y)
            h_line.strokeColor = PDF_CHART["tolerance"]
            h_line.strokeWidth = 0.8
            h_line.strokeDashArray = [2, 2]
            drawing.add(h_line)
            # "Unacceptable" callout — upper-right corner (right-anchored), matching
            # the web dual_lec_curve corner annotation (loss ≥ amount at ≥ prob).
            _unacc = String(
                plot.x + plot.width - 2,
                plot.y + plot.height - 9,
                f"Unacceptable (loss ≥ {safe_money_format(tol_amt, _lec_rc, compact=True)} at ≥ {tol_prob:.1%})",
                fontSize=7,
                fillColor=PDF_CHART["tolerance"],
            )
            _unacc.textAnchor = "end"
            drawing.add(_unacc)
            # "Tolerance" callout near the (tol_amt, tol_prob) crosshair point.
            drawing.add(
                String(
                    v_x + 4,
                    h_y + 4,
                    f"Tolerance: {safe_money_format(tol_amt, _lec_rc, compact=True)} @ {tol_prob:.1%}",
                    fontSize=7,
                    fillColor=PDF_CHART["tolerance"],
                )
            )

    # T3M-4-B1 (defensive): same conditional pattern as _epc_chart — LEC always
    # carries both series for current data, but guard against future code paths
    # that omit one series (e.g. a without-controls-only view).
    has_lec_without = bool(data.lec_without)
    has_lec_with = bool(data.lec_with)
    if has_lec_without and has_lec_with:
        lec_legend_text = "Without controls (orange)  ·  With controls (blue)"
    elif has_lec_with:
        lec_legend_text = "With controls (blue)"
    elif has_lec_without:
        lec_legend_text = "Without controls (orange)"
    else:
        lec_legend_text = None
    if lec_legend_text is not None:
        drawing.add(String(60, 260, lec_legend_text, fontSize=8))
    return drawing


def _epc_chart(data: RunReportData, *, width: float = 480.0) -> Drawing:
    """Dual EPC — linear-x (exceedance probability) + log-y (loss).

    Inverts ``_lec_chart``: x is the exceedance-probability axis (0..1,
    formatted as %; equals 1 - persisted percentile), y is loss in $ on a
    log scale. Same color convention: without-controls is orange
    (inherent), with-controls is blue (residual). The persisted EPC tuples
    carry (percentile, loss); this renderer flips x to ``1 - percentile``
    so the chart reads as "X% chance the loss exceeds $Y".

    ``width`` parameterizes the outer Drawing width — see ``_lec_chart``
    for the rationale (full-width = 500, side-by-side cell = 250).
    """
    import math

    drawing = Drawing(width, 280)
    plot = LinePlot()
    plot.x = 50
    plot.y = 50
    plot.width = max(width - 70.0, 100.0)
    plot.height = 200

    floor_y = 1.0

    def _flip_log(pts: list[tuple[float, float]]) -> list[tuple[float, float]]:
        # Flip x = percentile -> 1 - percentile (exceedance probability),
        # transform y to log10($).
        return [(1.0 - x, math.log10(max(y, floor_y))) for (x, y) in pts]

    # Build series list only from non-empty sources — reportlab fails with
    # IndexError when an empty series is included in plot.data (SINGLE runs
    # carry epc_with only; epc_without is [] for SINGLE).
    _without = _flip_log(data.epc_without)
    _with = _flip_log(data.epc_with)
    epc_series = [s for s in [_without, _with] if s]
    plot.data = epc_series if epc_series else [[]]
    # Color assignments: without (orange/inherent) first when present,
    # with (brand blue solid) second (or first if without is absent).
    _color_idx = 0
    if _without:
        plot.lines[_color_idx].strokeColor = PDF_CHART["inherent"]
        _color_idx += 1
    if _with:
        plot.lines[_color_idx].strokeColor = PDF_CHART["residual"]

    plot.xValueAxis.valueMin = 0.0
    plot.xValueAxis.valueMax = 1.0
    plot.xValueAxis.labelTextFormat = lambda v: f"{v * 100:.0f}%"
    # Abbreviated currency on log-y, same as the LEC chart. P3: use reporting_code.
    _epc_rc = getattr(data, "reporting_code", "USD")
    plot.yValueAxis.labelTextFormat = lambda v: safe_money_format(10**v, _epc_rc, compact=True)
    drawing.add(plot)

    # Tolerance overlay — vertical lines at tol_prob and (1-tol_prob), horizontal
    # at tol_amt (log-y). PDF-only light-blue central "likely range" band, NOT a
    # red quadrant (see the block below, and the PDF/web-divergence note there).
    tolerance = getattr(data, "loss_tolerance", None)
    # Issue #265: gate on `is not None` (see _lec_chart) so a valid 0.0 isn't dropped.
    if (
        tolerance
        and tolerance.get("amount") is not None
        and tolerance.get("probability") is not None
    ):
        all_ys = [math.log10(max(y, floor_y)) for (_x, y) in data.epc_without + data.epc_with]
        if all_ys:
            ymin, ymax = min(all_ys), max(all_ys)
            tol_amt = tolerance["amount"]
            tol_prob = tolerance["probability"]
            y_log = math.log10(max(tol_amt, floor_y))
            y_frac = (y_log - ymin) / (ymax - ymin) if ymax > ymin else 0.0
            v_x = plot.x + tol_prob * plot.width  # left edge of the likely-range band
            v_x2 = plot.x + (1.0 - tol_prob) * plot.width  # right edge (1 - tol_prob)
            h_y = plot.y + y_frac * plot.height
            # Light-blue "likely range" band brackets the central (1 - 2*tol_prob)
            # of the exceedance-probability axis (tol_prob .. 1-tol_prob). It is
            # added AFTER the curves, so (reportlab z-order = insertion order) it
            # renders as a translucent wash OVER them — the low alpha (0.10) keeps
            # the curves legible. This replaced the original PDF-only red upper-right
            # "unacceptable" quadrant, which shaded the wrong region.
            #
            # PDF/web divergence (epic #547 P3): the web EPC (chart.html::
            # dual_epc_curve) dropped this band — and the "Unacceptable" callout
            # below — in P1's first-party-SVG port, for design-system conformance
            # (tolerance is conveyed there by the tolerance-marker circle alone).
            # The PDF intentionally RETAINS both: a static report can carry more
            # annotation detail than an interactive page, so the PDF and web EPCs
            # are deliberately no longer identical on this chart. Do not "fix" the
            # PDF to match web by deleting the band/callout.
            band_w = v_x2 - v_x
            if band_w > 0:
                band = Rect(v_x, plot.y, band_w, plot.height)
                # Web blue-500 @ low alpha (rgba(59,130,246,0.06)); bumped slightly
                # for print legibility.
                band.fillColor = colors.Color(59 / 255.0, 130 / 255.0, 246 / 255.0, 0.10)
                band.strokeColor = None
                drawing.add(band)
            # Vertical dotted lines at BOTH band edges (tol_prob and 1 - tol_prob).
            for _vx in (v_x, v_x2):
                _vline = Line(_vx, plot.y, _vx, plot.y + plot.height)
                _vline.strokeColor = PDF_CHART["tolerance"]
                _vline.strokeWidth = 0.8
                _vline.strokeDashArray = [2, 2]
                drawing.add(_vline)
            # Horizontal dotted line at the tolerance loss amount.
            h_line = Line(plot.x, h_y, plot.x + plot.width, h_y)
            h_line.strokeColor = PDF_CHART["tolerance"]
            h_line.strokeWidth = 0.8
            h_line.strokeDashArray = [2, 2]
            drawing.add(h_line)
            # "Unacceptable" callout — upper-RIGHT corner (right-anchored). The
            # unacceptable region is loss ≥ amount AND exceedance-prob ≥ probability
            # (identical condition to the LEC, which is this chart transposed). The
            # web EPC has no counterpart to this callout post-#547 P1 — see the
            # PDF/web divergence note on the band above.
            _epc_unacc = String(
                plot.x + plot.width - 2,
                plot.y + plot.height - 9,
                f"Unacceptable (loss ≥ {safe_money_format(tol_amt, _epc_rc, compact=True)} at ≥ {tol_prob:.1%})",
                fontSize=7,
                fillColor=PDF_CHART["tolerance"],
            )
            _epc_unacc.textAnchor = "end"
            drawing.add(_epc_unacc)
            # "Tolerance" callout near the (tol_prob, tol_amt) crosshair point.
            drawing.add(
                String(
                    v_x + 4,
                    h_y + 4,
                    f"Tolerance: {safe_money_format(tol_amt, _epc_rc, compact=True)} @ {tol_prob:.1%}",
                    fontSize=7,
                    fillColor=PDF_CHART["tolerance"],
                )
            )

    # T3M-4-B1: build legend conditionally on which series are non-empty.
    # SINGLE runs have epc_without=[] so only the with-controls series is drawn;
    # omitting the missing series from the legend keeps it honest.
    has_without = bool(data.epc_without)
    has_with = bool(data.epc_with)
    if has_without and has_with:
        legend_text = "Without controls (orange)  ·  With controls (blue)"
    elif has_with:
        legend_text = "With controls (blue)"
    elif has_without:
        legend_text = "Without controls (orange)"
    else:
        legend_text = None
    if legend_text is not None:
        drawing.add(String(50, 260, legend_text, fontSize=8))
    return drawing


