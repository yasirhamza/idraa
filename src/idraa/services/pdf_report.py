def _humanize_enum(value: str) -> str:
    """Humanize a stored enum/slug for operator-facing display.

    Underscores → spaces, then title-case (e.g. ``process_manufacturing`` →
    ``Process Manufacturing``, ``mining`` → ``Mining``). Empty string passes
    through unchanged so callers can keep their own ``or "—"`` fallbacks.
    """
    if not value:
        return value
    return value.replace("_", " ").strip().title()


