        # control's parenthesized acronym (e.g. "MFA") so a narrow column shows a
        # compact code instead of a mid-word-truncated name; when no acronym
        # exists, use the full name and let the Paragraph wrap on word boundaries.
        # Polish-2: short codes are pre-assigned across ALL controls
        # (`_short_codes`, deduped by first-seen order) so a LATER duplicate
