def _truncate(s: str, n: int) -> str:
    return s if len(s) <= n else s[: n - 1] + "…"


# P7: a parenthesized acronym like "Multi-factor Authentication (MFA)" — the
# capture is 2-8 chars of upper-case letters / digits / a few joiners so codes
# like "MFA", "ESP", "EDR", "SIEM", "PAM" or "IAM/PAM" match but ordinary
# lower-case parentheticals ("(legacy)") do not.
# Polish-2: the capture requires at least one LETTER (lookahead), so a bare
# year like "(2024)" -- all digits, no letters -- is not mistaken for an
# acronym and does not become a column header.
_ACRONYM_RE = re.compile(r"\((?=[A-Z0-9./&-]*[A-Z])([A-Z0-9][A-Z0-9./&-]{1,7})\)")


def _control_short_code(name: str) -> str | None:
    """Return the parenthesized acronym in *name* (e.g. 'MFA'), or None.

    Used for attribution-matrix column headers so a wide matrix shows compact
    codes instead of mid-word-truncated names; the full names are surfaced in a
    legend note. Returns None when no acronym is present (including a
    letter-free parenthetical like a bare year, e.g. "(2024)") so the caller
    falls back to word-boundary wrapping of the full name.
    """
    m = _ACRONYM_RE.search(name)
    return m.group(1) if m else None


def _assign_control_short_codes(names: Sequence[str]) -> list[str | None]:
    """Assign a display short code per control name, deduped by first-seen order.

    Each name's parenthesized acronym (`_control_short_code`) becomes its
    short code UNLESS an EARLIER name in `names` already claimed that same
    code -- in which case this (later) name gets None, so the caller falls
    back to the full-name Paragraph for it. Polish-2: without this, two
    differently-named controls sharing an acronym (e.g. "Multi-factor Auth
    (MFA)" and "Multi-Factor Authentication (MFA)") would render identical
    "MFA" column headers (indistinguishable) and the legend's "MFA = ..."
    line would only describe one of them (inaccurate for the other).
    """
    seen: set[str] = set()
    assigned: list[str | None] = []
    for name in names:
        code = _control_short_code(name)
        if code and code not in seen:
            seen.add(code)
            assigned.append(code)
        else:
            assigned.append(None)
    return assigned


