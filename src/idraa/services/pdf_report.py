        _h1("Distribution statistics & tail risk", styles),
        Spacer(1, 0.15 * inch),
        table,
        Spacer(1, 0.1 * inch),
        Paragraph(rl_escape(_DIST_STATS_DEFINITIONAL_NOTE), note_style),
        Spacer(1, 0.05 * inch),
